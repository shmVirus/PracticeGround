void main () {
    /// Unary - Increment and Decrement
    int i = 0;
    i++; // postfix increment
    ++i; // prefix increment
    i--; // postfix decrement
    --i; // prefix decrement
    
    print('\x1B[38;5;43mfrom Slide\x1B[0m');
    var x = 30;
    print(x++);
    var y = 25;
    print(++y);
    var z = 10;
    print(--z);
    var u = 12;
    print(u--);

    print('\x1B[38;5;159mfrom Recording\x1B[0m');
    var t=10;
    print(t++);
    print(++t);
    print(--t);
    print(t--);
}
